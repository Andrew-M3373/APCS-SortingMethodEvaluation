
public class SortingMethod {

	private String title;
	private float value;
	public SortingMethod(String t, float v) {
		title = t;
		value = v;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
}
